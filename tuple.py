# Tuple
# - ordered(index)
# - Immutable(cannot be changed)
# - allows duplicated
# Common methods
# - index
# - count


my_tuple = ("Windows", "Linux", "Mac OS", "Unix", "Windows")
print(type(my_tuple))
print('count of the element "Windows":', my_tuple.count("Windows"))
print('index of the element "Windows": ', my_tuple.index("Windows"))
